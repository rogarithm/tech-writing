1. 얼기 쉬운 장소나 전열기 옆에는 설치하지 말아야 한다.

fb> Do not install the product in a place that can be easily frozen or near heat-generating equipment.

2. 습기가 많은 곳(욕실, 풀장 등), 물(빗물)이 튀는 곳 및 먼지가 많은 곳에는 설치하지 말아야 한다.

fb> Do not install the product in a place where there is much moisture (bathroom, swimming pool, etc.) or dust, or water (or raindrop) is spitting.

3. 케이블의 색상과 제품의 연결부위 색상이 바뀌지 않게 연결해야 한다.

fb> Make sure that all colors on the cables match the ones on the ports of the product.

4. 설치 시 벽면으로부터 15cm 이상 사이를 두어야 한다. 벽면에서 너무 가까우면 냉각 효율이 떨어질 수 있다.

fb> Have a gap (over 15 cm) from the wall when installing the product. If no space from the wall, it may decrease the cooling efficiency.

5. 경사진 장소에 설치하지 말아야 한다. 평평하고 견고한 장소에 설치할 것을 권장한다.

fb> Do not place the product on a slopy place. Be sure to install it on a flat and hard floor.

6. 제품을 눕혀서 운반한 경우 제품을 세운 후 반드시 1시간 이상 사용하지 말아야 한다. 냉각콤프의 안정화를 위해 시간이 필요하기 때문이다.

fb> After moving the product with maintaining a reclining position, do not operate the product for over an hour for stabilization of the refrigeration compressor.
