안전을 위한 주의사항 - 사용 관련 2

1. 레이저를 직접 쐬거나 반사된 빛을 쐬었을 때 눈에 부상을 입을 수 있다.

as> Exposure to direct beam or reflection from a laser may cause an eye injury.
