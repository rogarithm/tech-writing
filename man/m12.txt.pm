2. 제품의 포트(구멍)나 통풍구 등을 막지 말고, 이물질이 들어가지 않게 한다.

as> Do not close port (hole) or airflow hole of the product, and make sure foreign matter don't get inserted.

3. 촛불, 담뱃불 등을 제품 위에 올려 두어선 안 된다.

as> Do not place lighted candles or burning cigarettes on the product.

4. 고장의 위험이 있으며, 올려놓은 물건이 떨어져서 다치거나 제품이 손상될 수 있다.

as> It may cause a product defect, and dropped material may cause injuries or damage to the product.

5. 온도가 상승하면 제품의 모양이 변형될 수 있다.

as> The increase in temperature may cause deformation of the product.

6. 드라이브가 동작 중일 때는 Eject 버튼을 눌러서는 안 된다.

as> Do not push the Eject button while the drive is running.

7. 연결선에 걸려 넘어지면 상해를 입을 수 있다.

as> Tripping over a connection cable may cause an injury.
