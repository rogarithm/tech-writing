안전을 위한 주의사항 - 유지보수 관련 2

1. 허가 받지 않은 변경으로 인해 작동 권한이 박탈될 수 있다.

as> You may lose the operating authority due to unauthorized modification.

2. 철수하기 전 작업장을 청소해야 한다.

as> Clean the work area before leaving.

3. 매달 전 시스템에 대한 평가를 수행해야 한다.

as> An assessment for the entire system must be conducted every month.

4. 때를 닦아서 제거하도록 한다.

as> Remove dirt by wiping.

5. 제품을 자성이 있는 곳이나 자성의 영향을 받는 곳에 보관해서는 안 된다.

as> Do not keep the product in a place near or affected by magnetic fields.
