◊section{accuracy}66

Inaccurate information makes users upset, and not to read the document anymore. If a document directs users to do tasks inaccurately, the more the users have done things inaccurately, the more they'll be upset about the document and the product.

◊bold{Write information only when you understand it, and then verify it.}

Sometimes you can easily understand the subject that you'll be able to write quickly. But there's times you can't. Even technical experts can't help you, you must do your job. In that case, start with what you do understand to write your first draft.

It could be helpful to include questions to the experts.

If you get pressured by not understanding well, you don't need to understand the subject as well as the developers. Understanding too well could be a problem, because you might not understand why users have problem with the product. This can make your document hard to read and understand for readers.

When you write procedures, you need to understand how users are likely to use the procedures to perform relevant tasks.

The best way to understand a technical subject that you are writing about is to use the product as a user would.

◊bold{Keep up with technical changes.}

Technical information should be up-to-date. It could be outdated easily. It's better not to use terms strongly related to change of products or release, because you'll need to modify them along the changes. So don't use as much as you can, but if you have to, careful to modify them.

Because it's easier to update Web-based document, users expect this document to be updated for regular basis. Let users know about specifications of updates: when you'll update the document, what is changed from last updates, and/or making a way to get feedbacks from users.

◊bold{Maintain consistency of all information about a subject.}

When there's multiple places the information is located, you may need to reuse some part of the information. If the part is short and unlikely to change in the future, just copy and paste would be sufficient. For longer one, and if you don't want to copy, use single source approach. That is, use the same source, but different outputs.

